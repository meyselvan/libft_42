#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	size_t			len;
	char			*new;

	len = ft_strlen(s) + 1;
	new = (char *)malloc(len * sizeof(char));
	i = 0;
	if (new == NULL)
		return (NULL);
	if (!s && !f)
		return (0);
	while (s[i])
	{
		new[i] = f(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
