#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*p;

	i = 0;
	p = (unsigned char *)s;
	while (i < n && p[i] != (unsigned char)c)
		i++;
	if (i < n && p[i] == (unsigned char)c)
		return (p + i);
	return (NULL);
}
